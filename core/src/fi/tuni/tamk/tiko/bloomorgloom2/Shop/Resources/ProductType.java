package fi.tuni.tamk.tiko.bloomorgloom2.Shop.Resources;

/**
 * Houses information for products.
 */
public class ProductType {
    private int id;
    private String name;

    /**
     * Gets a product's ID.
     *
     * @return Product's ID.
     */
    public int getId() {
        return id;
    }

    /**
     * Gets product's name.
     *
     * @return Product's name.
     */
    public String getName() {
        return name;
    }
}
